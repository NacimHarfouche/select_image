const listImgElt = document.querySelectorAll("li"), spanElt = document.querySelector("span");
spanElt.textContent = 0;

/*************************************************************************************************/
/******************************************* FONCTIONS *******************************************/
/*************************************************************************************************/
(() => {
	for (let li of listImgElt) {
			li.addEventListener("click", function(){
				this.classList.toggle("selected");
				spanElt.textContent = document.querySelectorAll(".selected").length;
			});
	}
})();

